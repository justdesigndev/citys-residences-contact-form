"use client"

import * as React from "react"
import { Check, ChevronsUpDown } from "lucide-react"

import { cn } from "@/lib/utils"
import { Button } from "@/components/ui/button"
import {
  Command,
  CommandEmpty,
  CommandGroup,
  CommandInput,
  CommandItem,
  CommandList,
  CommandSeparator,
} from "@/components/ui/command"
import { Popover, PopoverContent, PopoverTrigger } from "@/components/ui/popover"

export interface ComboboxOption {
  value: string
  label: string
}

interface ComboboxProps {
  options: ComboboxOption[]
  priorityOptions?: ComboboxOption[]
  value?: string
  onValueChange?: (value: string) => void
  placeholder?: string
  searchPlaceholder?: string
  emptyMessage?: string
  disabled?: boolean
  className?: string
  triggerClassName?: string
  contentClassName?: string
  itemClassName?: string
}

export function Combobox({
  options,
  priorityOptions = [],
  value = "",
  onValueChange,
  placeholder = "Select...",
  searchPlaceholder = "Search...",
  emptyMessage = "No results found.",
  disabled = false,
  className,
  triggerClassName,
  contentClassName,
  itemClassName,
}: ComboboxProps) {
  const [open, setOpen] = React.useState(false)
  const triggerRef = React.useRef<HTMLButtonElement>(null)
  const [contentWidth, setContentWidth] = React.useState<number | undefined>(undefined)

  // Combine priority and regular options for finding selected option
  const allOptions = [...priorityOptions, ...options]
  const selectedOption = allOptions.find((option) => option.value === value)

  // Filter out priority options from regular options to avoid duplicates
  const regularOptions = options.filter(
    (option) => !priorityOptions.some((priority) => priority.value === option.value)
  )

  React.useEffect(() => {
    if (open && triggerRef.current) {
      setContentWidth(triggerRef.current.offsetWidth)
    }
  }, [open])

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        <Button
          ref={triggerRef}
          variant='outline'
          role='combobox'
          aria-expanded={open}
          disabled={disabled}
          className={cn("w-full justify-between", triggerClassName, className)}
        >
          {selectedOption ? selectedOption.label : placeholder}
          <ChevronsUpDown className='ml-2 h-4 w-4 shrink-0 opacity-50 text-bricky-brick' />
        </Button>
      </PopoverTrigger>
      <PopoverContent
        className={cn("p-0", contentClassName)}
        style={contentWidth ? { width: `${contentWidth}px` } : undefined}
        align='start'
      >
        <Command>
          <CommandInput placeholder={searchPlaceholder} className='h-9' />
          <CommandList>
            <CommandEmpty>{emptyMessage}</CommandEmpty>
            <CommandGroup>
              {priorityOptions.length > 0 && (
                <>
                  {priorityOptions.map((option) => (
                    <CommandItem
                      key={option.value}
                      value={option.value}
                      onSelect={(currentValue) => {
                        const newValue = currentValue === value ? "" : currentValue
                        onValueChange?.(newValue)
                        setOpen(false)
                      }}
                      className={itemClassName}
                    >
                      {option.label}
                      <Check className={cn("ml-auto h-4 w-4", value === option.value ? "opacity-100" : "opacity-0")} />
                    </CommandItem>
                  ))}
                  {regularOptions.length > 0 && <CommandSeparator className='my-1' />}
                </>
              )}
              {regularOptions.map((option) => (
                <CommandItem
                  key={option.value}
                  value={option.value}
                  onSelect={(currentValue) => {
                    const newValue = currentValue === value ? "" : currentValue
                    onValueChange?.(newValue)
                    setOpen(false)
                  }}
                  className={itemClassName}
                >
                  {option.label}
                  <Check
                    className={cn(
                      "ml-auto h-4 w-4 text-bricky-brick",
                      value === option.value ? "opacity-100" : "opacity-0"
                    )}
                  />
                </CommandItem>
              ))}
            </CommandGroup>
          </CommandList>
        </Command>
      </PopoverContent>
    </Popover>
  )
}
